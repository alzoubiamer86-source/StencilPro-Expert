    CONSTRAINT chk_user_preferences_type
        CHECK (preference_type IN ('string','boolean','integer','json'))
);

COMMENT ON TABLE user_preferences IS
    'Per-engineer application preferences (theme, units, defaults, display settings). '
    'Not audited — personal UX settings.';

