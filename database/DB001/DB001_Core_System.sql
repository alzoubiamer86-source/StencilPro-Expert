COMMENT ON TABLE audit_log IS
    'Append-only immutable audit trail. Partitioned by occurred_at (monthly). '
    'Retained for 7 years minimum (automotive IATF 16949, medical ISO 13485, '
    'aerospace AS9100 regulatory requirement). No UPDATE or DELETE permitted by any role.';

COMMENT ON COLUMN audit_log.id IS 'BIGSERIAL sequence — guarantees strict chronological ordering.';
COMMENT ON COLUMN audit_log.organization_id IS 'NULL for system-level events not tied to an organization.';
