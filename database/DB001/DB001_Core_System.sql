COMMENT ON COLUMN audit_log.old_values IS 'JSONB snapshot of row before UPDATE or DELETE. NULL for INSERT.';
COMMENT ON COLUMN audit_log.new_values IS 'JSONB snapshot of row after INSERT or UPDATE. NULL for DELETE.';
COMMENT ON COLUMN audit_log.changed_fields IS 'Array of column names that changed during UPDATE.';

-- Create initial partition covering current and next year
DO $$
DECLARE
    start_year  INT := EXTRACT(YEAR FROM NOW())::INT;
    start_month INT := EXTRACT(MONTH FROM NOW())::INT;
    part_start  DATE;
    part_end    DATE;
    part_name   TEXT;
    m           INT;
BEGIN
    -- Create 3 months of partitions: prior month, current month, next month
    FOR m IN -1..2 LOOP
        part_start := DATE_TRUNC('month', NOW() + (m || ' months')::INTERVAL)::DATE;
        part_end   := (part_start + INTERVAL '1 month')::DATE;
        part_name  := 'audit_log_' || TO_CHAR(part_start, 'YYYY_MM');

        EXECUTE FORMAT(
            'CREATE TABLE IF NOT EXISTS %I '
            'PARTITION OF audit_log '
            'FOR VALUES FROM (%L) TO (%L)',
            part_name, part_start, part_end
        );
    END LOOP;
END;
$$;

