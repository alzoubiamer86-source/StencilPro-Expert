-- ---------------------------------------------------------------------------
-- Shared helper: set updated_at on row modification
-- (Referenced by triggers defined in DB001_Triggers.sql)
-- ---------------------------------------------------------------------------

CREATE OR REPLACE FUNCTION fn_set_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$;

COMMENT ON FUNCTION fn_set_updated_at() IS
    'Generic trigger function: sets updated_at = NOW() before any UPDATE.';

