-- TABLE: feature_flags
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS feature_flags (
    id                      UUID        NOT NULL DEFAULT gen_random_uuid(),
    flag_key                VARCHAR(100) NOT NULL,
    description             TEXT        NOT NULL,
    is_enabled_globally     BOOLEAN     NOT NULL DEFAULT FALSE,
    enabled_for_org_ids     UUID[]      NOT NULL DEFAULT '{}',
    enabled_for_tiers       TEXT[]      NOT NULL DEFAULT '{}',
    rollout_percentage      INTEGER     NULL,
    created_at              TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at              TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    created_by              UUID        NULL,
    updated_by              UUID        NULL,

    CONSTRAINT pk_feature_flags
        PRIMARY KEY (id),

    CONSTRAINT uq_feature_flags_key
        UNIQUE (flag_key),

    CONSTRAINT chk_feature_flags_rollout
        CHECK (rollout_percentage IS NULL OR
               (rollout_percentage >= 0 AND rollout_percentage <= 100))
);

COMMENT ON TABLE feature_flags IS
    'Feature toggle system for progressive feature rollout. '
    'Controls which features are visible per organization, subscription tier, '
    'or globally.';

COMMENT ON COLUMN feature_flags.rollout_percentage IS
    '0–100 for gradual percentage-based rollout. NULL = not using percentage rollout.';

