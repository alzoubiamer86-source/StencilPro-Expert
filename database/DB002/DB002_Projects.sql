-- ---------------------------------------------------------------------------
-- TABLE: customers
-- ---------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS customers (
    id                              UUID            NOT NULL DEFAULT gen_random_uuid(),
    organization_id                 UUID            NOT NULL,
    name                            VARCHAR(255)    NOT NULL,
    code                            VARCHAR(20)     NOT NULL,
    industry_segment                VARCHAR(50)     NOT NULL DEFAULT 'industrial',
    required_ipc_class              VARCHAR(10)     NOT NULL DEFAULT 'class_2',
    regulatory_requirements         TEXT[]          NOT NULL DEFAULT '{}',
    requires_signed_reports         BOOLEAN         NOT NULL DEFAULT FALSE,
    approved_paste_ids              UUID[]          NOT NULL DEFAULT '{}',
    approved_stencil_material_ids   UUID[]          NOT NULL DEFAULT '{}',
    special_requirements_notes      TEXT            NULL,
    contact_name                    VARCHAR(255)    NULL,
    contact_email                   VARCHAR(255)    NULL,
    contact_phone                   VARCHAR(30)     NULL,
    website_url                     TEXT            NULL,
    is_active                       BOOLEAN         NOT NULL DEFAULT TRUE,
    is_deleted                      BOOLEAN         NOT NULL DEFAULT FALSE,
    deleted_at                      TIMESTAMPTZ     NULL,
    created_at                      TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    updated_at                      TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    created_by                      UUID            NULL,
    updated_by                      UUID            NULL,

    CONSTRAINT pk_customers
        PRIMARY KEY (id),

    CONSTRAINT uq_customers_org_code
        UNIQUE (organization_id, code),

    CONSTRAINT fk_customers_organization
        FOREIGN KEY (organization_id)
        REFERENCES organizations (id)
        ON DELETE RESTRICT,

    CONSTRAINT chk_customers_industry_segment
        CHECK (industry_segment IN (
            'automotive','medical','consumer','industrial',
            'aerospace','defense','mixed','other'
        )),

    CONSTRAINT chk_customers_required_ipc_class
        CHECK (required_ipc_class IN ('class_1','class_2','class_3')),

    CONSTRAINT chk_customers_code_format
        CHECK (code ~ '^[A-Z0-9_\-]{1,20}$'),

    CONSTRAINT chk_customers_contact_email_format
        CHECK (
            contact_email IS NULL OR
            contact_email ~* '^[A-Za-z0-9._%+\-]+@[A-Za-z0-9.\-]+\.[A-Za-z]{2,}$'
        ),

    CONSTRAINT chk_customers_soft_delete
        CHECK (
            (is_deleted = FALSE AND deleted_at IS NULL) OR
            (is_deleted = TRUE  AND deleted_at IS NOT NULL)
        )
);

COMMENT ON TABLE customers IS
    'External customers or internal business units for whom PCB assemblies are manufactured. '
    'Customer requirements (IPC class, approved materials) cascade to all projects '
    'and drive engineering rule activation. Scoped to organization.';

COMMENT ON COLUMN customers.id IS 'UUID primary key. Generated by application before insert.';
COMMENT ON COLUMN customers.organization_id IS 'Owning organization. RLS enforces isolation.';
COMMENT ON COLUMN customers.code IS 'Short uppercase code used in file naming and report headers (e.g., FORD, MEDCO). Unique per organization.';
COMMENT ON COLUMN customers.required_ipc_class IS 'Minimum IPC class this customer requires. Projects under this customer cannot use a lower class. Enforced by application and DB trigger.';
COMMENT ON COLUMN customers.regulatory_requirements IS 'Array of regulatory standards required (e.g., {RoHS, REACH, MIL-STD-2000}). Drives material constraint rules.';
COMMENT ON COLUMN customers.approved_paste_ids IS 'Array of solder_paste UUIDs that this customer has approved. If populated, rule engine flags use of non-listed pastes.';
COMMENT ON COLUMN customers.approved_stencil_material_ids IS 'Array of stencil_material UUIDs approved by this customer.';
COMMENT ON COLUMN customers.requires_signed_reports IS 'When TRUE, generated reports must carry an engineer signature before delivery to customer.';

