    change_summary          TEXT            NOT NULL,
    change_reason           VARCHAR(50)     NOT NULL DEFAULT 'engineering_change',
    authored_by_engineer_id UUID            NOT NULL,
    approved_by_engineer_id UUID            NULL,
    approved_at             TIMESTAMPTZ     NULL,
    project_snapshot        JSONB           NOT NULL DEFAULT '{}',
    is_current              BOOLEAN         NOT NULL DEFAULT FALSE,
    created_at              TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    updated_at              TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    created_by              UUID            NULL,
    updated_by              UUID            NULL,

    CONSTRAINT pk_project_revisions
        PRIMARY KEY (id),

    CONSTRAINT uq_project_revisions_project_number
        UNIQUE (project_id, revision_number),

    CONSTRAINT uq_project_revisions_project_code
        UNIQUE (project_id, revision_code),

    CONSTRAINT fk_project_revisions_project
        FOREIGN KEY (project_id)
        REFERENCES projects (id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_project_revisions_organization
        FOREIGN KEY (organization_id)
        REFERENCES organizations (id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_project_revisions_authored_by
        FOREIGN KEY (authored_by_engineer_id)
        REFERENCES engineers (id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_project_revisions_approved_by
        FOREIGN KEY (approved_by_engineer_id)
        REFERENCES engineers (id)
        ON DELETE RESTRICT,

    CONSTRAINT chk_project_revisions_type
        CHECK (revision_type IN (
            'initial_release','minor_change','major_change',
            'emergency_change','eco'
        )),

    CONSTRAINT chk_project_revisions_reason
        CHECK (change_reason IN (
            'engineering_change','customer_request','defect_correction',
            'cost_reduction','regulatory_update','process_improvement','other'
        )),

    CONSTRAINT chk_project_revisions_revision_number
        CHECK (revision_number > 0),

    CONSTRAINT chk_project_revisions_approval
        CHECK (
            (approved_at IS NULL AND approved_by_engineer_id IS NULL) OR
            (approved_at IS NOT NULL AND approved_by_engineer_id IS NOT NULL)
        )
);

COMMENT ON TABLE project_revisions IS
    'Versioned snapshots of a project at each formal revision point. '
    'Provides a complete revision history for regulatory audit (IATF 16949, ISO 13485, AS9100). '
    'project_snapshot is a JSONB copy of key project metadata at time of revision. '
    'Only one revision per project may have is_current = TRUE.';

COMMENT ON COLUMN project_revisions.revision_number IS 'Sequential integer revision counter (1, 2, 3...). Unique per project.';
COMMENT ON COLUMN project_revisions.revision_code IS 'Human-readable revision code (A, B, C... or 1.0, 1.1...). Unique per project.';
COMMENT ON COLUMN project_revisions.project_snapshot IS 'JSONB snapshot of project metadata at time of this revision. Immutable after creation. Enables regulatory audit of historical project state.';
COMMENT ON COLUMN project_revisions.is_current IS 'TRUE for the most recent active revision. Enforced unique by partial index.';

-- Partial unique index: only one current revision per project
CREATE UNIQUE INDEX IF NOT EXISTS uq_project_revisions_current
    ON project_revisions (project_id)
    WHERE is_current = TRUE;

