        PRIMARY KEY (id),

    CONSTRAINT uq_projects_org_number
        UNIQUE (organization_id, project_number),

    CONSTRAINT fk_projects_organization
        FOREIGN KEY (organization_id)
        REFERENCES organizations (id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_projects_customer
        FOREIGN KEY (customer_id)
        REFERENCES customers (id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_projects_product
        FOREIGN KEY (product_id)
        REFERENCES products (id)
        ON DELETE RESTRICT,

    CONSTRAINT fk_projects_lead_engineer
        FOREIGN KEY (lead_engineer_id)
        REFERENCES engineers (id)
        ON DELETE RESTRICT,

    CONSTRAINT chk_projects_status
        CHECK (status IN (
            'draft','active','on_hold','in_review',
            'approved','completed','archived'
        )),

    CONSTRAINT chk_projects_phase
        CHECK (phase IN (
            'npi','pre_production','production',
            'eco','sustaining','end_of_life'
        )),

    CONSTRAINT chk_projects_ipc_class
        CHECK (ipc_class IN ('class_1','class_2','class_3')),

    CONSTRAINT chk_projects_target_yield
        CHECK (target_yield_pct IS NULL OR
               (target_yield_pct >= 0 AND target_yield_pct <= 100)),

    CONSTRAINT chk_projects_dates
        CHECK (
            target_completion_date IS NULL OR
            start_date IS NULL OR
            target_completion_date >= start_date
        ),

    CONSTRAINT chk_projects_actual_completion
        CHECK (
            actual_completion_date IS NULL OR
            start_date IS NULL OR
            actual_completion_date >= start_date
        ),

    CONSTRAINT chk_projects_soft_delete
        CHECK (
            (is_deleted = FALSE AND deleted_at IS NULL) OR
            (is_deleted = TRUE  AND deleted_at IS NOT NULL)
        )
);

